public class Dolphin implements MarineAnimal {

    @Override
    public void swim() {
        System.out.println(" Дельфин плавает грациозно и быстро, выполняя пируэты");
    }

    public void jump() {
        System.out.println(" Дельфин прыгает над водой");
    }

    public void breathe() {
        System.out.println(" Дельфин дышит воздухом через дыхало");
    }

    public void play() {
        System.out.println(" Дельфин играет с сородичами");
    }
}